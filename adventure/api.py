@csrf_exempt
@api_view(["GET"])
def rooms(request):
    rooms = Room.objects.all()
    roomsArray = []
    for i in range(len(rooms)):
        roomsArray.append({
            'id': rooms[i].id, 
            'title': rooms[i].title, 
            'description': rooms[i].description,
            'n_to': rooms[i].n_to, 
            's_to': rooms[i].s_to, 
            'e_to': rooms[i].e_to, 
            'w_to': rooms[i].w_to
        })
    return JsonResponse({
        'rooms': roomsArray
    }, safe=True)


